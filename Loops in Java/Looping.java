public class Looping {
    public static void main(String args[]){
        //for loop 

        for(int a= 0;a < 11;a = a+1){
            // System.out.println("Hello World");
            System.out.print(a +" ");
        }


        //while loop 
        
        int j = 0;
        while(j<11){
            System.out.println(j);
            j++;
        }
       
        //do while loop 
        int i = 0 ;
        do{
            System.out.println(i);
            i++;
        }while(i<11);
    }
}
   